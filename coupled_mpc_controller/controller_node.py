"""
controller_node.py — DUT25 Coupled MPC Controller (ROS 2 Node)
==============================================================
Author:  Ian Pichs (ifp2107) — Columbia University
Course:  ELEN 6760 Discrete Control Systems, Spring 2026
Project: Evolution of Control Architectures for High-Speed Autonomous Racing

PURPOSE
-------
ROS 2 node implementing a Coupled Nonlinear MPC for lateral + longitudinal
control of the DUT25 autonomous car on the skidpad mission. This node is a
drop-in replacement for both mpc_python_exec AND longitudinal_control,
solving steering and throttle jointly via a single NLP at each timestep.

The node uses the acados solver generated by ocp_definition.py (run offline).
At startup it loads the pre-compiled C solver (build=False) so initialization
takes milliseconds, not seconds.

WHAT IS COUPLED HERE
--------------------
The Decoupled MPC (mpc_python) solves steering only, with vx fixed. This node
solves for [steer_rate, throttle] jointly, with vx as a dynamic state, under
the friction circle constraint:

    (ax / ax_max)^2 + (ay / ay_max)^2 <= 1

This forces the optimizer to trade lateral grip against longitudinal
acceleration at circle entries — the precise moment where the LQR and
Decoupled MPC rely on saturated limits rather than optimal planning.

ROS INTERFACE (identical to LQR node — drop-in replacement)
------------------------------------------------------------
Subscribes:
    /controllers/opt_requests  (controller_msgs/OptRequest)  ~40 Hz
        Reference waypoints + current vehicle state x0.
    /controllers/accel_request (controller_msgs/AccRequest)  ~250 Hz
        Speed setpoint + current speed.

Publishes:
    /controllers/opt_results   (controller_msgs/OptResult)
        N+1 = 41 predicted steer angles used by skidpad_manager for
        latency compensation (interp1d over linspace(0, 1.0, 41)).
    /controllers/long          (controller_msgs/PIDErrors)
        Throttle command: .input field read by skidpad_manager as
        the acceleration command for TrajectorySetpoints.

SOLVE LOOP (per OptRequest callback, ~40 Hz)
-------------------------------------------
1.  Extract current vehicle state x0 from msg.x0.
2.  Find nearest waypoint in the reference path.
3.  Build reference trajectory yref[k] for k=0..N from waypoints ahead.
4.  Set initial state constraint and stage references in the solver.
5.  Call solver.solve() — one SQP_RTI iteration (~1-3 ms).
6.  Extract steer sequence (41 values) and first throttle command.
7.  Publish OptResult (steer) and cache throttle for AccRequest callbacks.

WARM STARTING
-------------
The solver is warm-started from the previous solution by shifting the optimal
trajectory one step forward (x[k] <- x[k+1], u[k] <- u[k+1]) before each
solve. This keeps the initial guess close to the new optimum and ensures
RTI converges in one iteration on a smoothly evolving trajectory.
"""

