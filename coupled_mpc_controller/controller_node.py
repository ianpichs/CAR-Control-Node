# These mirror the tunable parameters in ocp_definition.py.
# They are also exposed as ROS parameters for launch-file overrides.
# =============================================================================

VX_TARGET    = 11.25   # [m/s] skidpad cruise speed (linearization point)
LOOKAHEAD_STEPS = 5    # waypoints ahead for curvature feedforward into yref
                        # (smaller than LQR's 40 — MPC already plans ahead
                        # over the full N-step horizon; this shifts the
                        # reference origin slightly to match the MPC horizon)

# ifp2107: Warm-start mismatch threshold [m].
# If the warm-start predicted position at the mid-horizon (k=N//2) is further
# than this from the reference at that stage, the prior solution is stale and
# the warm-start is reset to neutral. This fires at the skidpad circle
# changeover where the reference path changes curvature sign — without the
# reset, the optimizer anchors to the previous circle trajectory and produces
# an extreme first-step solution (hard brake + max steer). With the reset the
# solver starts from the measured state and finds a smooth transition. The
# threshold (2.0 m) sits above normal tracking error (~0.3 m) but below the
# ~3.3 m mid-horizon mismatch at changeover.
WARM_START_MISMATCH_THRESHOLD = 3.0   # [m]


# =============================================================================
# ifp2107: HELPERS
# =============================================================================

def normalize_angle(angle: float) -> float:
    """
    ifp2107: Wrap an angle to [-pi, pi].

    PSEUDO-CODE
    -----------
    Input:  angle [rad] (any range)
    Output: angle [rad] in [-pi, pi]

    Prevents discontinuities at ±180° when computing heading error.
    """
    return (angle + math.pi) % (2.0 * math.pi) - math.pi


def compute_curvature(ref_idx: int, msg: OptRequest) -> float:
    """
    ifp2107: Estimate signed path curvature kappa = d_theta/d_s at ref_idx.

    PSEUDO-CODE
    -----------
    Input:  ref_idx — waypoint index in msg arrays
            msg     — OptRequest containing path arrays
    Output: kappa [1/m], positive = left turn, negative = right turn

    Identical implementation to lqr_pid_controller for consistency.
    Uses finite difference of heading angle divided by arc length.
    """
    n = len(msg.pos_x)
    if n < 2:
        return 0.0
    i      = min(ref_idx, n - 2)
    theta0 = math.atan2(msg.head_sin[i],     msg.head_cos[i])
    theta1 = math.atan2(msg.head_sin[i + 1], msg.head_cos[i + 1])
    ds     = math.hypot(
        msg.pos_x[i + 1] - msg.pos_x[i],
        msg.pos_y[i + 1] - msg.pos_y[i],
    )
    if ds < 1e-6:
        return 0.0
    return normalize_angle(theta1 - theta0) / ds


